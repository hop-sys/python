# calculate the S.I given that principal is 45000, rate is 7% and the time taken is 8 years.(si = p * r * t/100)


def simpleinterest():
    p = 45000   # principal
    r = 7       # rate in percent
    t = 8       # time in years

    si = (p * r * t) / 100
print("The simple interest is ", simpleinterest)
print(simpleinterest)



